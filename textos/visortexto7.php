          <p>  Llamaron a la puerta. Era ella. Me dijo: he dejado a Juan. Yo disimulé la sorpresa que me causaba su visita y la invité a pasar. Se sentó en el sofá y me dijo lo que todos sabíamos: que Juan era idiota.<br />
—Ya, bueno, pero era idiota desde el primer día —dije yo.<br />
—Sí, aprovecha ahora para regodearte —contestó ella.<br />
—Oye, lo que tenemos en común tú y yo es que nos enamoramos de idiotas.<br />
—Bonita manera de llamarme idiota.<br />
—Pero si sabes que te lo digo de broma. Que yo beso donde pisas y todo eso. Si te quiero a pesar de las ladillas de campeonato que me pegaste aquella vez.<br />
—Aquello fue un accidente.<br />
—Lo sé, nunca te he acusado de premeditación.<br />
—Además, no negarás que valió la pena. Te encantó.<br />
—¿Tener ladillas?<br />
—Follarme, imbécil.<br />
—La verdad es que no me importaría repetir. ¿Tú qué opinas?<br />
—No he venido para eso.<br />
—Ya. Y de todas las personas que conoces en esta ciudad me has elegido a mí para hablar de tu ex. No cuela. A mí me parece que has venido para echar un polvo y, si eso, hablar después.<br />
—Vale, sí. Pero quería que fuera más disimuladamente.<br />
—Sí, que acabáramos en la cama sin saber cómo, pero me parece que estoy demasiado sobrio para eso. Además, nosotros no tenemos necesidad de andarnos por las ramas, ya nos conocemos lo suficiente.<br />
—Bueno, ¿pero podemos hacer como que ha sido un accidente?<br />
—Claro. Tú quítate la ropa, ya buscaremos excusas luego.<br /></p>
